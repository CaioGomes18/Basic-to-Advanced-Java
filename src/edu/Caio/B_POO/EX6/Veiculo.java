package edu.Caio.B_POO.EX6;

public class Veiculo {
    private String nomeDaFabricante;
    private String modelo;
    private String ano;


    public String getNomeDaFabricante() {
        return nomeDaFabricante;
    }

    public void setNomeDaFabricante(String nomeDaFabricante) {
        this.nomeDaFabricante = nomeDaFabricante;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getAno() {
        return ano;
    }

    public void setAno(String ano) {
        this.ano = ano;
    }


}
